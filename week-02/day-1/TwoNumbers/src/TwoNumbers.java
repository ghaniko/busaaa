public class TwoNumbers {
    public static void main(String[] args) {
        int number1 = 13;
        int number2 = 22;


        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(13. / 22.);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);
    }
}
